/*******************************************************************************
  * @函数名称	HardFault_Handler
  * @函数说明   硬件错误发生的中断处理函数
  * @输入参数   无
  * @输出参数   无
  * @返回参数   无
*******************************************************************************/
void HardFault_Handler(void)
{
    SerialPutString("ERROR!!!!! \n\r");
    //当硬件错误发生时进入死循环 
    while (1)
    {
        //点亮LED指示错误
        GPIO_ResetBits(GPIOB,LED2 | LED3 | LED4 | LED5);//点亮LED2
    }
}

